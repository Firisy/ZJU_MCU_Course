ORG 0000H
SJMP START
ORG 0030H

START:
MOV R3,30H
MOV R0,#31H ;数据指针
MOV 41H,#00H
MOV 42H,#00H
MOV 43H,#00H

LOOP:
CJNE @R0,#00H,NOTZERO ;若不为0则跳转
INC 43H
SJMP NEXT

NOTZERO:
CJNE @R0,#80H,NEQUAL    ;若不为80H则跳转
INC 42H
SJMP NEXT

NEQUAL:     
JC POSITIVE ;若比80H小，则为正数，CY=1，则跳转
INC 42H     ;CY=0,否则为负数
SJMP NEXT

POSITIVE:
INC 41H     

NEXT:
INC R0
DJNZ R3,LOOP

SJMP $

END
